"""
korzystajac z funkcji round postaraj sie zmodyfikowac ponizszy kod tak aby wynikiem byla prawda

0.1 + 0.1 + 0.1 = 0.3
"""

wynik = 0.1 + 0.1 + 0.1
wynik = round(wynik,1)
print(wynik)
print(wynik == 0.3 )

